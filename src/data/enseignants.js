const ExcelDateToJSDate = (date) => {
    let converted_date = new Date(Math.round((date - 25569) * 864e5));
    converted_date = String(converted_date).slice(4, 15)
    date = converted_date.split(" ")
    let day = date[1];
    let month = date[0];
    month = "JanFebMarAprMayJunJulAugSepOctNovDec".indexOf(month) / 3 + 1
    if (month.toString().length <= 1)
        month = '0' + month
    let year = date[2];
    return String(day + '/' + month + '/' + year.slice(2, 4))
}


 



export const getEnsData = () => {


    const dateFields = ['DateDeNaissance', 'DateDeObtention', 'DataDeNomination']

    return Enseigs.slice(0,Enseigs.length).map(ens => {
        const updatedEns = {}

        //
        updatedEns['Nom'] = 'Nom ' + ens.id

        for (const [fieldName, fieldValue] of Object.entries(ens)) {
            if(dateFields.includes(fieldName)) {
                updatedEns[fieldName] = ExcelDateToJSDate(fieldValue)

            } else {
                updatedEns[fieldName] = fieldValue
            }
        }
        return updatedEns
    })
}


export const searchEnsData = (value) => {

    return getEnsData().filter(ens => {
        for (const [fieldName, fieldValue] of Object.entries(ens)) {
            if(fieldValue?.toString().toLowerCase().indexOf(value?.toString().toLowerCase()) !== -1 ) {
                return true
            }
        }
        return false
    })
}